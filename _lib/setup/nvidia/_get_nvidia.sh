_detect_chroot() {
	# https://unix.stackexchange.com/questions/14345/how-do-i-tell-im-running-in-a-chroot
	if [ "$(stat -c %d:%i /)" != "$(stat -c %d:%i /proc/1/root/.)" ]
	then
		_messageFAIL
		_stop 1
		exit 1
		exit
		return 1
		return
	fi
	
	if systemctl status sddm 2>&1 | head -n 2 | grep -i 'chroot'
	then
		_messageFAIL
		_stop 1
		exit 1
		exit
		return 1
		return
	fi
	
	return 0
}
